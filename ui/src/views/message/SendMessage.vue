<template>
  <div class="content">
    <a-spin :spinning="loading">
      <a-form :form="form" @submit="handleSubmit">
        <a-form-item label="Topic">
          <a-select class="topic-select" @change="handleTopicChange" show-search option-filter-prop="children"
            v-decorator="[
              'topic',
              {
                rules: [{ required: true, message: '请选择一个topic!' }],
              },
            ]" placeholder="请选择一个topic">
            <a-select-option v-for="v in topicList" :key="v" :value="v">
              {{ v }}
            </a-select-option>
          </a-select>
        </a-form-item>
        <a-form-item label="分区">
          <a-select class="type-select" show-search option-filter-prop="children" v-model="selectPartition"
            placeholder="请选择一个分区">
            <a-select-option v-for="v in partitions" :key="v" :value="v">
              <span v-if="v == -1">默认</span> <span v-else>{{ v }}</span>
            </a-select-option>
          </a-select>
        </a-form-item>
        <a-form-item label="消息头">
          <table>
            <thead>
              <tr>
                <th>Key</th>
                <th>Value</th>
              </tr>
            </thead>
            <tbody>
              <tr v-for="(row, index) in rows" :key="index">
                <td><input type="headerKey" v-model="row.headerKey" /></td>
                <td><input type="headerValue" v-model="row.headerValue" /></td>
                <td><a-button type="primary" @click="deleteRow(index)">Delete</a-button></td>
              </tr>
            </tbody>
            <a-button type="primary" @click="addRow">Add Row</a-button>
          </table>
        </a-form-item>
        <a-form-item label="消息Key">
          <a-input v-decorator="['key', { initialValue: 'key' }]" />
        </a-form-item>
        <a-form-item label="消息体" has-feedback>
          <a-textarea v-decorator="[
            'body',
            {
              rules: [
                {
                  required: true,
                  message: '输入消息体!',
                },
              ],
            },
          ]" placeholder="输入消息体!" />
        </a-form-item>
        <a-form-item label="发送的消息数">
          <a-input-number v-decorator="[
            'num',
            {
              initialValue: 1,
              rules: [
                {
                  required: true,
                  message: '输入消息数!',
                },
              ],
            },
          ]" :min="1" :max="32" />
        </a-form-item>
        <a-form-item :wrapper-col="{ span: 12, offset: 5 }">
          <a-button @click="submitForm" type="primary" html-type="submit"> 提交 </a-button>
        </a-form-item>
      </a-form>
    </a-spin>
  </div>
</template>

<script>
import request from "@/utils/request";
import { KafkaTopicApi, KafkaMessageApi } from "@/utils/api";
import notification from "ant-design-vue/lib/notification";
export default {
  name: "SendMessage",
  components: {},
  props: {
    topicList: {
      type: Array,
    },
  },
  data() {
    return {
      form: this.$form.createForm(this, { name: "message_send" }),
      loading: false,
      partitions: [],
      selectPartition: undefined,
      rows: [{ headerKey: '', headerValue: '' }],
    };
  },
  methods: {
    getTopicNameList() {
      request({
        url: KafkaTopicApi.getTopicNameList.url,
        method: KafkaTopicApi.getTopicNameList.method,
      }).then((res) => {
        if (res.code == 0) {
          this.topicList = res.data;
        } else {
          notification.error({
            message: "error",
            description: res.msg,
          });
        }
      });
    },
    getPartitionInfo(topic) {
      this.loading = true;
      request({
        url: KafkaTopicApi.getPartitionInfo.url + "?topic=" + topic,
        method: KafkaTopicApi.getPartitionInfo.method,
      }).then((res) => {
        this.loading = false;
        if (res.code != 0) {
          notification.error({
            message: "error",
            description: res.msg,
          });
        } else {
          this.partitions = res.data.map((v) => v.partition);
          this.partitions.splice(0, 0, -1);
        }
      });
    },
    handleTopicChange(topic) {
      this.selectPartition = -1;
      this.getPartitionInfo(topic);
    },
    addRow() {
      this.rows.push({ HeaderKey: '', HeaderValue: '' });
    },
    deleteRow(index) {
      this.rows.splice(index, 1);
    },
    submitForm() {
    //  e.preventDefault();
      this.form.validateFields((err, values) => {
        if (!err) {
          const param = Object.assign({}, values, {
            partition: this.selectPartition,
            headers: this.rows,
          });
          this.loading = true;
          request({
            url: KafkaMessageApi.sendWithHeader.url,
            method: KafkaMessageApi.sendWithHeader.method,
            data: param,
          }).then((res) => {
            this.loading = false;
            if (res.code == 0) {
              this.$message.success(res.msg);
            } else {
              notification.error({
                message: "error",
                description: res.msg,
              });
            }
          });
        }
      });
    },
  },
  created() {
    this.getTopicNameList();
  },
};
</script>

